function calcPrice(productCode, w, h, qty) {

    // --------------------------------
    // 제품 불러오기
    // --------------------------------

    const product =
        PRODUCTS[productCode];

    // --------------------------------
    // 면적 계산
    // --------------------------------

    const area =
        w * h;

    // --------------------------------
    // 500매 가격 공식
    // 1 ~ 500매
    // --------------------------------

    const price500 =

        (
            product.base500
            +
            (
                product.areaRate * area
            )
            -
            100
        );

    // --------------------------------
    // 1000매 가격 공식
    // 501 ~ 1000매
    // --------------------------------

    const price1000 =

        price500
        *
        (
            1.445
            -
            (
                0.0000002 * area
            )
        );

    // --------------------------------
    // 최종가격 계산
    // --------------------------------

    let finalPrice = 0;

    // 1 ~ 500매
    if (qty <= 500) {

        finalPrice =
            price500;

    }

    // 501 ~ 1000매
    else if (qty <= 1000) {

        finalPrice =
            price1000;

    }

    // 2000매 이상
    else {

        // 1000매 단위 올림
        const multiplier =

            Math.ceil(
                qty / 1000
            );

        finalPrice =

            price1000
            *
            multiplier;
    }

    // --------------------------------
    // 마진률 추가
    // --------------------------------

    finalPrice =

        finalPrice
        *
        (
            1
            +
            (
                product.marginRate
                / 100
            )
        );

    // --------------------------------
    // 배송비 추가
    // --------------------------------

    finalPrice =

        finalPrice
        +
        DELIVERY_FEE;

    // --------------------------------
    // 반올림 반환
    // --------------------------------

    return Math.round(
        finalPrice
    );
}